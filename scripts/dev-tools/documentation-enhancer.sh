validate_documentation() {
    print_section "Documentation Validation"

    local total_files=0
    local valid_files=0
    local error_count=0

    # Validate all markdown files
    while IFS= read -r -d '' file; do
        ((total_files++))
        print_info "Validating: ${file#$DOCS_ROOT/}"

        if validate_markdown_syntax "$file"; then
            ((valid_files++))
        else
            ((error_count++))
        fi
    done < <(find "$DOCS_ROOT" -name "*.md" -print0)

    # Validation summary
    local pass_rate=0
    if [[ $total_files -gt 0 ]]; then
        pass_rate=$((valid_files * 100 / total_files))
    fi

    echo ""
    print_section "Validation Results"
    echo "ðŸ“Š Documentation Validation Summary:"
    echo "   Total Files: $total_files"
    echo "   Valid Files: $valid_files"
    echo "   Files with Issues: $error_count"
    echo "   Validation Rate: ${pass_rate}%"
    echo ""

    if [[ $error_count -eq 0 ]]; then
        print_success "âœ… All documentation validated successfully!"
        return 0
    else
        print_warning "âš ï¸ Documentation has $error_count file(s) with issues"
        return 1
    fi
}

